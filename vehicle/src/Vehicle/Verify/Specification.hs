-- Query meta data

data QueryMetaData = QueryData
  { metaNetwork :: MetaNetwork,
    variableInfo :: QueryVariableInfo
  }
  deriving (Show, Generic)

instance ToJSON QueryMetaData

instance FromJSON QueryMetaData

